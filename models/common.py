class EDSRConv(torch.nn.Module):
    def __init__(self, in_ch, out_ch):
        super(EDSRConv, self).__init__()
        self.conv = torch.nn.Sequential(
            torch.nn.Conv2d(in_ch, out_ch, 3, padding=1),
            torch.nn.ReLU(inplace=True),
            torch.nn.Conv2d(out_ch, out_ch, 3, padding=1),
            )

        self.residual_upsampler = torch.nn.Sequential(
            torch.nn.Conv2d(in_ch, out_ch, kernel_size=1, bias=False),
            )

    def forward(self, input):
        return self.conv(input)+self.residual_upsampler(input)

class SuperResolution(nn.Module):
    def __init__(self, ch, c1=128, c2=512, sync_bn=True, freeze_bn=False):
        super(SuperResolution, self).__init__()

        if sync_bn == True:
            BatchNorm = SynchronizedBatchNorm2d
        else:
            BatchNorm = nn.BatchNorm2d

        #self.attention = AttentionModel(128)
        self.sr_decoder = Decoder(c1,c2,BatchNorm)
        self.up_sr_1 = nn.ConvTranspose2d(64, 64, 2, stride=2) 
        self.up_edsr_1 = EDSRConv(64,64)
        self.up_sr_2 = nn.ConvTranspose2d(64, 32, 2, stride=2) 
        self.up_edsr_2 = EDSRConv(32,32)
        self.up_sr_3 = nn.ConvTranspose2d(32, 16, 2, stride=2) 
        self.up_edsr_3 = EDSRConv(16,16)
        self.up_conv_last = nn.Conv2d(16,ch,1)


        self.freeze_bn = freeze_bn

    def forward(self, low_level_feat,x):
        x_sr= self.sr_decoder(x, low_level_feat) #[1, 64, 64, 64]
        #attention_map,x_sr = self.attention(x_sr)
        x_sr_up = self.up_sr_1(x_sr) #[1, 64, 128, 128]
        x_sr_up=self.up_edsr_1(x_sr_up) #[1, 64, 128, 128]

        x_sr_up = self.up_sr_2(x_sr_up) #[1, 32, 256, 256]
        x_sr_up=self.up_edsr_2(x_sr_up) #[1, 32, 256, 256]

        x_sr_up = self.up_sr_3(x_sr_up) #[1, 16, 512, 512]
        x_sr_up=self.up_edsr_3(x_sr_up) #[1, 16, 512, 512]
        x_sr_up=self.up_conv_last(x_sr_up)

        return x_sr_up

    def freeze_bn(self):
        for m in self.modules():
            if isinstance(m, SynchronizedBatchNorm2d):
                m.eval()
            elif isinstance(m, nn.BatchNorm2d):
                m.eval()

class Decoder(nn.Module):
    def __init__(self, c1,c2,BatchNorm):
        super(Decoder, self).__init__()
        
        self.conv1 = nn.Conv2d(c1, 48, 1, bias=False)
        self.bn1 = BatchNorm(48)
        self.relu = nn.ReLU()
        #self.pixel_shuffle = nn.PixelShuffle(4)
        #self.attention = AttentionModel(48+c2)
        self.last_conv = nn.Sequential(nn.Conv2d(48+c2, 256, kernel_size=3, stride=1, padding=1, bias=False),
                                       BatchNorm(256),
                                       nn.ReLU(),
                                       nn.Dropout(0.5),
                                       nn.Conv2d(256, 128, kernel_size=3, stride=1, padding=1, bias=False),
                                       BatchNorm(128),
                                       nn.ReLU(),
                                       nn.Dropout(0.1),
                                       nn.Conv2d(128, 64, kernel_size=1, stride=1))
        # self.last_conv1 = nn.Sequential(nn.Conv2d(48+c2, 256, kernel_size=3, stride=1, padding=1, bias=False),
